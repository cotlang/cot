test "schema metadata lookup" {
    const schema_source =
        \\@database testdb
        \\@version 1
        \\
        \\@table customer
        \\    id          ,ulid      @primary @auto
        \\    name        ,text(30)
        \\    created_at  ,datetime  @audit_created
        \\    updated_at  ,datetime  @audit_updated
        \\@endtable
        \\
        \\@table order
        \\    id          ,ulid      @primary @auto
        \\    customer_id ,ulid      @foreign(customer.id)
        \\@endtable
    ;

    const parser = @import("parser.zig");
    var schema = try parser.parseSchema(std.testing.allocator, schema_source);
    defer schema.deinit();

    var metadata = try SchemaMetadata.init(std.testing.allocator, &schema);
    defer metadata.deinit();

    // Test table lookup
    try std.testing.expect(metadata.getTable("customer") != null);
    try std.testing.expect(metadata.getTable("order") != null);
    try std.testing.expect(metadata.getTable("nonexistent") == null);

    // Test primary key detection
    try std.testing.expect(metadata.isPrimaryKey("customer", "id"));
    try std.testing.expect(!metadata.isPrimaryKey("customer", "name"));

    // Test auto-generated detection
    try std.testing.expect(metadata.isAutoGenerated("customer", "id"));
    try std.testing.expect(!metadata.isAutoGenerated("customer", "name"));

    // Test audit field detection
    try std.testing.expect(metadata.isAuditCreated("customer", "created_at"));
    try std.testing.expect(!metadata.isAuditCreated("customer", "updated_at"));
    try std.testing.expect(metadata.isAuditUpdated("customer", "updated_at"));

    // Test getting all audit fields
    const audit_fields = try metadata.getAuditFields(std.testing.allocator, "customer");
    defer std.testing.allocator.free(audit_fields);
    try std.testing.expectEqual(@as(usize, 2), audit_fields.len);

    // Test getting foreign keys
    const fk_fields = try metadata.getForeignKeys(std.testing.allocator, "order");
    defer std.testing.allocator.free(fk_fields);
    try std.testing.expectEqual(@as(usize, 1), fk_fields.len);
    try std.testing.expectEqualStrings("customer_id", fk_fields[0].name);
}

test "table info extraction" {
    const schema_source =
        \\@database testdb
        \\@version 1
        \\
        \\@table product
        \\    id       ,ulid       @primary @auto
        \\    name     ,text(50)   @not_null
        \\    price    ,decimal    @default(0)
        \\@endtable
    ;

    const parser = @import("parser.zig");
    var schema = try parser.parseSchema(std.testing.allocator, schema_source);
    defer schema.deinit();

    var info = try getTableInfo(std.testing.allocator, &schema.tables[0]);
    defer freeTableInfo(std.testing.allocator, &info);

    try std.testing.expectEqualStrings("product", info.name);
    try std.testing.expectEqual(@as(usize, 3), info.fields.len);

    // Check field info
    try std.testing.expectEqualStrings("id", info.fields[0].name);
    try std.testing.expect(info.fields[0].is_primary);
    try std.testing.expect(info.fields[0].is_auto);

    try std.testing.expectEqualStrings("name", info.fields[1].name);
    try std.testing.expect(info.fields[1].is_not_null);

    try std.testing.expectEqualStrings("price", info.fields[2].name);
    try std.testing.expectEqualStrings("0", info.fields[2].default_value.?);
}
